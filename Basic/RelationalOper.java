
public class RelationalOper {
    public static void main(String[] args) {
        int p = 15;
        int q = 20;
        System.out.println(p == q);
        System.out.println(p != q);
        System.out.println(p >= q);
        System.out.println(p <= q);
        System.out.println(p > q);
        System.out.println(p < q);
        System.out.println(p = q);


    }
    
}
